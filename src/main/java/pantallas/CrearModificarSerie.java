    /**
     * Carga las temporadas en su JList
     */
    private void actualizarTemporadas() {
        String[] temporadasImprimir = new String[capitulosTemporada.size()];
        for (int i = 0 ; i < temporadasImprimir.length ; i++) {
            temporadasImprimir[i] = "Temporada " + (i+1);
        }
        listaTemporadas.setListData(temporadasImprimir);
    }
    
    /**
     * Carga los capítulos de la temporada en el JList
     * 
     * @param temporadaSeleccionada temporada de la que hay que cargar los capitulos
     */
    private void actualizarCapitulos(int temporadaSeleccionada) {
        String[] capitulosImprimir = new String[capitulosTemporada.get(temporadaSeleccionada).size()];
        for (int i = 0 ; i < capitulosImprimir.length ; i++) {
            capitulosImprimir[i] = capitulosTemporada.get(listaTemporadas.getSelectedIndex()).get(i).getNombre();
        }
        listaCapitulos.setListData(capitulosImprimir);
    }

