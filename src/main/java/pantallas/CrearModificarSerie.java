    private void crearModificarContenidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crearModificarContenidoActionPerformed
        if (!imagenPortada.getText().equals("") && !titulo.getText().equals("") && !sinopsis.getText().equals("") && !actores.getText().equals("")) {
            
            // Busca el archivo seleccionado
            String ruta = imagenPortada.getText();
            String[] carpetasRuta;
            if (ruta.contains("\\"))
                carpetasRuta = ruta.split("\\\\");
            else carpetasRuta = ruta.split("/");
            
            // Lo copia a la carpeta del programa
            archivo.moverImagen(imagenPortada.getText(), "img/imgcontenidos/" + carpetasRuta[carpetasRuta.length - 1]);
            
            if (crear) {
                Serie contenidoAdd = new Serie(capitulosTemporada, titulo.getText(), sinopsis.getText(), 
                        (String) genero.getSelectedItem(), 
                        (String) anno.getSelectedItem(), actores.getText().split(", "), "img/imgcontenidos/" + carpetasRuta[carpetasRuta.length - 1]);

                ((Administrador) usuario).crearContenido(contenidoAdd, contenidos);
                
            } else {

                ((Administrador) usuario).modificarContenido((Serie) contenidoSeleccionado, capitulosTemporada,
                        titulo.getText(), sinopsis.getText(), (String) genero.getSelectedItem(), (String) anno.getSelectedItem(),
                        actores.getText().split(", "), "img/imgcontenidos/" + carpetasRuta[carpetasRuta.length - 1]);

            }
            
            archivo.guardarArchivo("contenidos.dat", contenidos);
            this.dispose();

        } else JOptionPane.showMessageDialog(this, "Por favor, rellene todos los campos", "ERROR", JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_crearModificarContenidoActionPerformed

    
