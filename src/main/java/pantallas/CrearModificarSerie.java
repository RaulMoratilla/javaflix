    // Borra el capítulo seleccionado en el JList
    private void borrarCapituloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_borrarCapituloActionPerformed

        if (!listaCapitulos.isSelectionEmpty()) {
           int indice = listaCapitulos.getSelectedIndex();
           int indiceTemporada = listaTemporadas.getSelectedIndex();
           capitulosTemporada.get(indiceTemporada).remove(indice);
           actualizarTemporadas();
           listaTemporadas.setSelectedIndex(indiceTemporada);
            
        } else JOptionPane.showMessageDialog(this, "No existen capitulos para borrar", "ERROR", JOptionPane.ERROR_MESSAGE);

    }//GEN-LAST:event_borrarCapituloActionPerformed

    // Añade capítulo a la temporada seleccionada
    private void addCapituloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCapituloActionPerformed

        try {

            String nombre = JOptionPane.showInputDialog(this, "Introduce el nombre del nuevo capítulo");
            if (!nombre.equals("")) {
                int duracion = Integer.parseInt(JOptionPane.showInputDialog(this, "Introduce la duración del nuevo capítulo"));
                capitulosTemporada.get(listaTemporadas.getSelectedIndex()).add(new Capitulo(nombre, duracion));
                actualizarCapitulos(listaTemporadas.getSelectedIndex());
            } else throw new NumberFormatException();

        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Datos mal introducidos. Capítulo no creado", "ERROR", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_addCapituloActionPerformed

    
    // Borra la temporada seleccionada y sus capítulos
    private void borrarTemporadaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_borrarTemporadaActionPerformed

        if (!listaTemporadas.isSelectionEmpty()) {
            int temporadaBorrar = listaTemporadas.getSelectedIndex();
            int eleccion = JOptionPane.showOptionDialog(this, "¿Está seguro de que desea borrar la temporada " + (temporadaBorrar + 1) + "?"
                + " Ten en cuenta que se borrarán sus capítulos y la temporada " + (temporadaBorrar + 2) + " pasará a ser la " + (temporadaBorrar + 1)
                + " etc.",
                "Borrar Temporada", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE, null, null, null);
            if (eleccion == 0) {
                capitulosTemporada.remove(temporadaBorrar);
                actualizarTemporadas();
                listaTemporadas.setSelectedIndex(0);

            }
        } else JOptionPane.showMessageDialog(this, "Ninguna Temporada Seleccionada", "ERROR", JOptionPane.ERROR_MESSAGE);

    }//GEN-LAST:event_borrarTemporadaActionPerformed

