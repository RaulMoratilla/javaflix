    private void botCrearContenidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botCrearContenidoActionPerformed
        
        // Pregunta si serie o película
        String[] tipoContenido = {"Serie", "Película"};
        int opcion = JOptionPane.showOptionDialog(this, "Elija el tipo de contenido que desea crear", "Tipo de contenido",
                JOptionPane.DEFAULT_OPTION, JOptionPane.PLAIN_MESSAGE, null, tipoContenido, tipoContenido[0]);
        switch (opcion) {
            case 0: {
                contenidoSeleccionado = null;
                this.dispose();
                CrearModificarSerie crearSerie = new CrearModificarSerie();
                crearSerie.setVisible(true);
                break;
            } case 1: {
                contenidoSeleccionado = null;
                this.dispose();
                CrearModificarPelicula crearPelicula = new CrearModificarPelicula();
                crearPelicula.setVisible(true);
                break;
            }
        }
        
    }//GEN-LAST:event_botCrearContenidoActionPerformed

    private void botModificarContenidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botModificarContenidoActionPerformed
        try {
            // Indice del contenido en el JList
            int indice = listaContenidos.getSelectedIndex();
            Contenido contenidoSeleccionadoMuestra = contenidoMostrar.get(indice);
            contenidoSeleccionado = contenidos.get(contenidos.indexOf(contenidoSeleccionadoMuestra));
            // Abre pantalla modificar contenido
            contenidoSeleccionado.crearModificarContenido();
            this.dispose();
        } catch (Exception e) {JOptionPane.showMessageDialog(this, "Ningún Contenido Seleccionado", "ERROR", JOptionPane.ERROR_MESSAGE);};
    }//GEN-LAST:event_botModificarContenidoActionPerformed

    private void botBorrarContenidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botBorrarContenidoActionPerformed
        
        // Indice del contenido en el JList
        int indice = listaContenidos.getSelectedIndex();
        if (indice != -1) {
            // Pregunta para asegurar
            String[] opciones = {"Sí", "No"};
            int eleccion = JOptionPane.showOptionDialog(this, "¿Está seguro de que quiere borrar el contenido? La acción es irreversible",
                    "Confirme la acción", JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE, null, opciones, opciones[1]);
            if (eleccion == 0) {

                Contenido contenidoSeleccionadoMuestra = contenidoMostrar.get(indice);
                contenidoSeleccionado = contenidos.get(contenidos.indexOf(contenidoSeleccionadoMuestra));
                // Borra contenido
                ((Administrador) usuario).borrarContenido(contenidoSeleccionado, contenidos);
                archivo.guardarArchivo("contenidos.dat", contenidos);
                actualizarContenidos();
                cargarContenidos(contenidoMostrar);
            } 
        } else JOptionPane.showMessageDialog(this, "Ningún Contenido Seleccionado", "ERROR", JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_botBorrarContenidoActionPerformed

    
