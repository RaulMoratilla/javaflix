    // Lee contenidos del archivo

    private void actualizarContenidos() {
        
        contenidos = (ArrayList<Contenido>) archivo.leerArchivo("contenidos.dat");
        contenidoMostrar = contenidos;
    }
    
    // Carga contenidos en el JList
    private void cargarContenidos(ArrayList<Contenido> contenidos) {
        
        String[] contenidosLista = new String[contenidos.size()];
        for (int i = 0 ; i < contenidosLista.length ; i++) {
            contenidosLista[i] = contenidos.get(i).toString();
        }
        listaContenidos.setListData(contenidosLista);
        
    }
    

